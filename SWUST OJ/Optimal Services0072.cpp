int a[10001];
void SORT(int *a,int n)
{
	for(int i=0;i<n-1;i++)
	{
		for(int j=0;j<n-1-i;j++)
		{
			if(a[j]>a[j+1])
			{
				int t=a[j];
				a[j]=a[j+1];
				a[j+1]=t;
			}
		}
	}
}
int main()
{
	int n;
	double sum=0;
	scanf("%d",&n);
	for(int i=0;i<n;i++)
		scanf("%d",&a[i]);
	SORT(a,n);
	for(int i=0;i<n;i++)
		sum+=(n-i)*a[i];
	printf("%.2lf\n",sum/n);
	return 0;
}

